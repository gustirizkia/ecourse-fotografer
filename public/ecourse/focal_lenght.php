                <p>Focal length adalah deskripsi dasar dari lensa kamera yang biasanya dinyatakan dalam bentuk milimeter (mm), menyadur Nikon USA.<br>
Ukuran tersebut bukanlah pengukuran panjang lensa yang sebenarnya. Melainkan, perhitungan jarak pusat optik lensa dengan sensor kamera.Panjang fokus film atau lensa pada kamera menentukan seberapa banyak pemandangan yang dapat ditangkap oleh kamera.Ukuran lensa sendiri diberi nama berdasarkan panjang fokusnya.<br>
<br>
Focal length adalah ukuran jarak antara lensa dengan sensor pada kamera. Lensa panjang yang vokalnya besar (misalnya 50mm) akan memberikan sudut pandang sempit, sehingga objek pada jarak jauh tampak terlihat besar di dalam foto.<br>
<br>
Bagi Anda yang ingin objek yang difoto terfokus tajam dan backgroundnya blur (efek foto bokeh), maka bisa menggunakan teknik ruang tajam sempit.Apabila ingin menghasilkan foto yang mempunyai ruang tajam sempit, maka Anda bisa merubah diafragma kamera menjadi besar ataupun f nya menjadi kecil.Jika Anda mengambil foto utama dan backgroundnya terlihat jelas, menggunakan teknik ruang tajam yang luas. Untuk menghasilkan gambar seperti itu, maka Anda bisa merubah diafragma kameranya menjadi kecil dan f nya besar.<br>
<br>
Biasanya panjang focal length juga bisa berpengaruh pada aperture, intensitas blur, dan fokus. Berikut pembahasannya:<br>
<br>
1. Pada Fokus<br>
Lensa sendiri terbagi menjadi 2 jenis yang berbeda yakni varifocal dan parfocal. Kalau varifocal, ketika Anda susah mengunci fokus pada focal length 33 mm. Kemudian berubah menjadi 100mm. Maka fokus yang sudah terkunci akan berubah menjadi tidak fokus dan Anda harus menyesuaikannya kembali.<br>
<br>
Sedangkan pada parfocal Anda tidak perlu lagi menyesuaikan fokus ketika mengganti focal length pada lensa.<br>
<br>
2. Pada Aperture<br>
Apabila Anda mau merubah focal length, maka secara otomatis aperture juga akan berubah. Misalnya di lensa 18-55m F/3.5-56. Ketika Anda memakai di angka 18m maka aperture maksimal yang dapat dipakai adalah F/3.5.<br>
<br>
Namun ketika Anda memakai 55mm, maka aperture maksimal yang bisa digunakan adalah F/5.6. Aperture yang bisa berubah-ubah ini disebut dengan aperture variable.<br>
<br>
Tapi, ada juga lensa yang mempunyai aperture tunggal. Sehingga aperturenya tidak bisa diubah ketika pergantian focal length. Contohnya pada lensa Sigma 18-35mm F1.8. Lensa seperti ini disebut dengan aperture fixed atau aperture constant.<br>
<br>
3. Pada Intensitas Blur<br>
Intensitas blur pada suatu gambar dipengaruhi oleh focal length. Jadi, semakin panjang ukuran focal length-nya yang Anda buat maka semakin blur juga dihasilkan oleh lensa. Begitu juga sebaliknya.

