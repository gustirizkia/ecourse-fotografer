ISO merupakan ukuran sensitivitas sensor terhadap cahaya. Ukuran dimulai
dari angka 50, 80, atau 100 dan akan berlipat ganda sampai 3200 atau bahkan lebih
besar lagi. ISO dengan ukuran angka kecil, berarti sensitivitas terhadap cahaya rendah, ISO dengan angka besar berarti sebaliknya. ISO dengan angka besar atau disebut juga
dengan ISO tinggi akan mengurangi kualitas gambar, karean munculnya bintik-bintik
yang dinamakan “noise”. Foto angka Nampak berbintik-bintik seperti pasir dan detail
Nabila et all, Jurnal Laporan Akhir Teknik Komputer Vo. 1 No. 1 September 2021 32
yang halus akan hilang. Tapi untuk kondisi yang sulit, seperti sedikit cahaya dalam
ruangan, ISO yang tinggi seringkali diperlukan.<br>
<br>

Fungsi ISO<br>

Sesuai dengan pengertiannya, fungsi ISO pada kamera berguna ketika memotret di tempat gelap (low light), dengan mengatur ISO semakin tinggi maka akan semakin kuat tingkat sensivitas cahaya pada sensor kamera.<br>

Misalnya saja, hasil foto dengan ISO 1.600 tentu lebih terang jika dibandingkan foto dengan ISO 100.<br>

Namun perlu di ingat, menentukan nilai ISO semakin tinggi akan beresiko tinggi kamera menghasilkan noise pada foto. Noise atau derau adalah suatu sinyal gangguan elektronis yang hadir kedalam foto, biasanya berbentuk bintik-bintik kecil yang mengganggu.<br>
