Eye Level<br>
Sudut pandang ini adalah sudut pandang atau angle yang umum digunakan. Pada angle ini lensa kamera dibidik sejajar dengan tinggi objek. Posisi dan arah kamera memandang objek yang akan dipotret layaknya mata kita melihat objek secara biasa. Pengambilan angle ini kebanyakan untuk memotret manusia dan aktifitasnya (Human interest).<br>
<br>
Low Angle<br>
Pada sudut pengambilan foto ini, kamera diposisikan lebih rendah dari objek. Low angle biasanya digunakan untuk menunjukkan kesan elegan, megah dan tangguh. Sudut pandang pemotretan ini sering juga diterapkan pada fotografi cityscape, contohnya pada foto gedung-gedung penacakar langit.<br>
<br>
High Angle <br>
Angle ini digunakan untuk menangkap kesan luas dari objek yang difoto. Pada angle ini kamera diposisikan lebih tinggi dari objek, sehingga memberi kesan kecil dari objek yang difoto. Dengan angle ini kita bisa memasukkan elemen-elemen pendukung komposisi ke dalam frame. Penerapan high angle bisa juga diterapkan pada foto pemandangan (landscape).<br>
<br>
Bird Eye View<br>
Anda bisa mencoba mendapatkan hasil yang berbeda dengan mengambil foto dari titik yang terletak diketinggian. Dalam sudut pemotretan ini, posisi objek berada di bawah atau lebih rendah dari kita berdiri. Dari sudut pandang ini, kita memiliki area pandang yang sangat luas, termasuk juga perspektif objek dan hubungannya dengan benda â€“ benda di sekelilingnya.<br>
<br>
Frog Eye View<br>
Pada pemotretan dengan angle ini kamera disejajarkan dengan tanah. Angle ini biasanya digunakan untuk objek yang posisinya di atas tanah. Untuk memotret dengan sudut pandang ini terkadang fotografer harus tiduran di tanah, untuk menghasilkan foto yang bagus.
Pada intinya sudut pengambilan gambar (angle) dalam fotografi adalah merupakan kreatifitas dari fotografer. Perlu kreatifitas dan mata jeli dari fotografer dalam melihat objek, untuk menghasilkan foto yang bagus.


