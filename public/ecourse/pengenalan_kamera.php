Untuk menciptakan sebuah gambar, sebuah kamera digital SLR (atau DSLR) akan membutuhkan 3 unsur berikut:<br>
<br>
ISO (ASA)<br>
Aperture (Diafragma)<br>
Speed (Kecepatan)<br>
<br>
