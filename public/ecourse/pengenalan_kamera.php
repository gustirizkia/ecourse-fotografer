ISO:<br>
Semakin tinggi ‘noise’ yang ada pada gambar. ‘Noise’ ini adalah bintik2 yang didapatkan pada sebuah gambar, yang sering nampak jika berfoto di dalam ruangan yang gelap menggunakan HP.
Gambar akan menjadi semakin terang – dikarenakan sensor menjadi lebih sensitif terhadap cahaya.
Kualitas gambar semakin menurun. Dikarenakan oleh noise yang mengganggu, maka berfoto dengan ISO yang tinggi akan mengurangi detail dari sebuah gambar.
Jadi, sebagai aturan yang standard,  gunakanlah ISO serendah mungkin yang bisa dicapai. Jika melakukan foto session di luar ruangan pada saat matahari terik, gunakanlah ISO 100-200. Untuk pemotretan di dalam ruangan yang dengan pencahayaan yg cukup, gunakanlah ISO 400 – 800. Dan pada saat melalukan photoshoot pada malam hari atau di ruangan yang gelap, gunakanlah ISO 1600 – 3200. Janganlah takut untuk menggunakan ISO yang tinggi, karena kamera digital sekarang sudah sangat canggih dan mampu mengantisipasi noise yang berlebihan.<br>

APERTURE (DIAFRAGMA)<br>
Aperture atau diafragma adalah “bukaan” dari lensa yang kita pakai. Bukaan ini diukur dengan istilah “f stop”, contohnya “f1.2, f2.0, f8.0, f11, dan sebagainya. Semakin kecil nomor f-stop, maka:<br>

bukaan akan menjadi lebih besar
lebih banyak cahaya yang dibolehkan masuk ke sensor
gambar akan menjadi lebih terang
“depth of field” atau ruang fokus akan menyempit.
Tiap lensa mempunyai f-stop yang berbeda. Misalnya, lensa 50mm f1.8 berarti lensa 50mm itu mempunyai diafragma f1.8 di bukaan yang terbesar. Lensa zoom seperti 18-55mm f3.5-5.6 berarti mempunyai bukaan terbesar di f3.5 pada saat menggunakannya di 18mm, dan mempunyai bukaan terbesar f5.6 pada saat meng-zoom di 55mm.<br>

Bukaan yang besar akan mengijinkan cahaya yang lebih banyak untuk masuk ke dalam kamera untuk direkam oleh sensor. Tak hanya itu, ruang fokus juga akan ikut terpengaruhi. “Depth of field” yang bagus untuk pemotretan model adalah yang sempit, agar modelnya saja yang fokus dan background nya blur. Maka itulah, gunakan bukaan yang besar saat foto model atau di mana anda ingin memisahkan sebuah objek dari latar nya. Untuk pemotretan landscape atau pemandangan, biasanya fotografer ingin untuk menangkap seluruh komposisi dengan tajam, maka gunakanlah f-stop yang kecil, yaitu f8.0 atau f20<br>

SPEED (KECEPATAN)<br>
Kecepatan yang dimaksud adalah seberapa cepatnya shutter kamera untuk meng-expose cahaya ke dalam sensor di kamera. Speed ini diukur dengan hitungan detik, misalnya 30″ pada kamera berarti 30 detik. Untuk speed yang lebih cepat, di kamera biasanya akan tertulis 125, yang sebenarnya berarti 1/125 detik (0.008 detik). Kemampuan kamera digital sekarang biasanya menjangkai 1/8000 detik, yang sering digunakan untuk memotret sebuah action seperti permainan olah raga.<br>

Semakin lamban kecepatan, maka:<br>

semakin banyak cahaya yang terekam oleh sensor
gambar akan menjadi semakin terang.
Tangan manusia biasanya mampu memengang kamera tanpa shake di speed 1/60 – 1/200, jadi jika anda memotret dengan cara memegang kamera (tanpa menggunakan tripod), upayakan agar kecepatan berada di 1/60 – 1/200. Jika menginginkan untuk dapat merekam action atau gerakan yang cukup cepat, seperti seorang menendang bola, gunakanlah speed di atas 1/200.<br>

Kesimpulan<br>

Demikianlah 3 unsur yang digunakan sebuah kamera untuk menciptakan gambar. Dengan perpaduan ISO, speed dan aperture, kita dapat menghasilkan foto yang sempurna. Jangan takut untuk bereksperimen dengan memadukan ketiga hal ini, karena masing2 mempunyai dampak yang bisa menjadikan fotografi anda lebih mengesankan lagi.<br>

Tips: Selalulah mengupayakan untuk menggunakan ISO terendah yang bisa dicapai, agar kualitas foto yang didapatkan maksimal dengan noise yang minimal.</p>
