                <p>Komposisi adalah seni untuk menciptakan harmoni pembagian bidang dengan memanfaatkan berbagai elemen visual yang tersedia yaitu garis, bentuk, cahaya, bayangan, warna dan tekstur (Sukarya, 2009:).<br>

Salah satu komposisi yang dapat diterapkan yaitu komposisi sepertiga bidang atau rule of thirds. Rule of thirds didapat dengan membagi bidang foto dalam tiga bagian vertikal dan tiga bagian horizontal. Rule of thirds membagi empat persegi panjang manjadi tiga bagian yang menghasilkan titik-titik kuat pada pertemuan garis vertikal dan horizontal.<br>
<br>
Filosofi dibalik rule of thirds sebenarnya adalah untuk menghindari komposisi simetris yang biasanya terkesan membosankan. Jika mengambil gambar dengan menyertakan garis cakrawala, hindari pembagian langsung ditengah dan perhatikan konsep bagi tiga itu. Usahakan membagi objek menjadi dua bagian, yang terdiri 2:3 objek yang memliki proporsi besar dan 1:3 objek dengan proporsi kecil (Mulyanto, 2007).<br>
<br>
Elemen Komposisi Fotografi<br>
Line (Garis)<br>
<br>
Garis merupakan unsur komposisi yang paling penting dalam foto. Tanpa ada garis, tidak akan ada bentuk, tanpa ada bentuk tidak aka nada wujud. Dan tanpa ada garis serta bentuk, tidak aka nada pola. Komposisi ingin menimbulkan kesan kedalaman dan kesan gerak pada objek. Garis juga dimaksudkan untuk membawa perhatian pengamat pada subjek utama. Garis dapat berupa lurus, melingkar, melengkung, yang memiliki kesan berbeda-beda. Berikut ini jenis- jenis garis dalam elemen fotografi garis.<br>
<br>
Garis Horizontal<br>
Garis horizontal dalam sebuah foto dapat memberi kesan stabilitas, tenang, permanen, dan kokoh. Foto horizontal yang populer adalah garis langit dan dataran atau lautan.
Garis Vertikal
Garis vertikal memberikan kesan kekuasaan dan tinggi. Sebut saja tiang, gedung bertingkat, pohon, monumen, sebagai contohnya. Hanya saja, untuk komposisi yang baik, hindari meletakkan garis vertikal secara kaku di tengah foto karena akan memberikan kesan membagi foto menjadi dua.<br>
<br>
Garis Diagonal<br>
Komposisi dengan garis diagonal akan terasa lebih dinamis. Garis ini memberi nafas yang membuat kesan lebih hidup. Garis diagonal juga mempertegas unsur prespektif pada foto. Garis diagonal akan melahirkan efek kedalaman atau tiga dimensi dalam sebuah foto.<br>
<br>
Garis Lengkung<br>
Garis lengkung bersifat luwes dan dinamis. Garis lengkung lebih hidup disbanding diagonal. Selain itu juga member kesan lembut, rileks, dan bergerak.

Bentuk adalah merupakan tatanandua dimensional, mulai dari titik, garis, dan pola. Bentuk juga termasuk kontras pencahayaan yang ekstrim seperti siluet, penonjolan detail benda, ataupun mengikutkan subjek menjadi garis luar atau outline dari sebuah tone warna tertentu.<br>
<br>
Pattern (Pola)<br>
Pola merupakan tatanan dari kelompok sejenis yang diulang untuk mengisi bagian tertentu di dalam bingkai foto. Pola merupakan kesan keseragaman, dengan pola yang diatur sedemikian rupa, maka akan membentuk presepsi dan kesan tersendiri. Terkadang suatu pola akan menampilkan kesan abstrak.<br>
<br>
Framing (Bingkai)<br>
Framing merupakan salah satu komposisi foto yang populer. Foto seolah-olah diberikan bingkai yang menggunakan objek-objek yang ada disekitar. Bisa saja dengan tumbuhan, pintu, jendela, terowongan dan lain-lain. Framing akan mengarahkan perhatian orang yang melihat foto akan langsung tertuju pada objek utama.


